    private void jButton_SelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SelecionarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos de Imagem", "jpg", "jpeg", "png", "gif");
        fileChooser.setFileFilter(filter);
        int result = fileChooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            origemGlobal = fileChooser.getSelectedFile();
        }
        String caminhoIcone = fileChooser.getSelectedFile().getPath();
        ImageIcon icon = new ImageIcon(caminhoIcone);
        //definindo um tamanho limite para a imagem 
        ImageIcon resizedIcon = new ImageIcon(icon.getImage().getScaledInstance(200, 200, java.awt.Image.SCALE_SMOOTH));
        jLabel_LogoImagem.setIcon(resizedIcon);
    }//GEN-LAST:event_jButton_SelecionarActionPerformed

    private void jButton_InserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_InserirActionPerformed
        try {
            Integer _id = -1;
            if (!jTextField_ID.getText().isBlank()) {
                _id = Integer.valueOf(jTextField_ID.getText());
            }
            
            Marca novaMarca = new Marca(_id, jTextField_Descricao.getText(), origemGlobal);
            controleDeMarca.incluir(novaMarca);
            origemGlobal = null;
            carregarMarcas();
            limparTela();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton_InserirActionPerformed

    private void jTable_TabelaDeMarcasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_TabelaDeMarcasMouseClicked
        jTextField_ID.setEnabled(false);
        int linha = jTable_TabelaDeMarcas.getSelectedRow();
        try {
            Marca marca = controleDeMarca.consulaMarca(Integer.valueOf((String) jTable_TabelaDeMarcas.getValueAt(linha, 0)));
            jTextField_ID.setText(marca.getId() + "");
            jTextField_Descricao.setText(marca.getDescricao());
            origemGlobal = new File(marca.getLogo().getPath());
            setImagemLogo(marca.getLogo().getPath());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jTable_TabelaDeMarcasMouseClicked

