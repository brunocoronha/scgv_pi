    private void carregarMarcas() {
        // Aqui a tabela é preenchida ordenada pelo ID obtido pelo
        // getID de cada marca registrada
        //
        try {
            ArrayList<Marca> listaDeMarca = controleDeMarca.listagemDeMarcas();
            Collections.sort(listaDeMarca, (Marca o1, Marca o2) -> {
                return o1.getId().compareTo(o2.getId());
            });
            DefaultTableModel model = (DefaultTableModel) jTable_TabelaDeMarcas.getModel();
            model.setNumRows(0);
            int x = 0;
            if (!listaDeMarca.isEmpty()) {
                for (Marca i : listaDeMarca) {
                    String[] dados = i.toString().split(";");
                    model.addRow(dados);
                    String imagePath = dados[2];
                    ImageIcon imageIcon = new ImageIcon(imagePath);
                    Image image = imageIcon.getImage();
                    Image scaledImage = image.getScaledInstance(18, 18, Image.SCALE_SMOOTH);
                    ImageIcon scaledImageIcon = new ImageIcon(scaledImage);
                    model.setValueAt(scaledImageIcon, x, 2);
                    jTable_TabelaDeMarcas.getColumnModel().getColumn(2).setCellRenderer(new ImageRenderer());
                    x++;
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(rootPane, erro.getMessage());
        }

    }

